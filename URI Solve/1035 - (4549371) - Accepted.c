#include <stdio.h>
int main()
{
    int A,B,C,D,P,Q;
    scanf("%d%d%d%d",&A,&B,&C,&D);
     P=C+D;
     Q=A+B;
    if(B>C && D>A && P>Q && C>0 && D>0 && A%2==0)
        printf("Valores aceitos\n");

        else
        printf("Valores nao aceitos\n");

    return 0;
}
