#include <stdio.h>

int main()
{
    char name;
    double salary,sell,temp,total;
    scanf("%s %lf %lf",&name,&salary, &sell);
    temp = (sell*15)/100;
    total = salary+temp;
    printf("TOTAL = R$ %.2lf\n",total);

    return 0;
}

