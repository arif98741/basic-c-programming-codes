#include <stdio.h>
int main()
{
    int num;
    int worked_hours;
    float amount;
    scanf("%d%d%f",&num,&worked_hours,&amount);

    printf("NUMBER = %d\n",num);
    printf("SALARY = U$ %.2f\n",worked_hours*amount);

    return 0;
}
