//Arithmetic operator
//Integer operator
//Real Operator

#include <stdio.h>
int main()
{
    int num1, num2,res;
    num1=5,num2=2;
    res=num1+num2;
    printf("%d + %d = %d\n", num1,num2,res);
    printf("%d %d = %d\n", num1,num2,num1%num2);
    printf("%d * %d = %d\n", num1,num2,num1*num2);
    printf("%d / %d = %d", num1,num2,num1/num2);






    return 0;
}
